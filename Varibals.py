# Python Variables - Examples

# String variables
name = "John"
greeting = "Hello, World!"

# Integer variables
age = 25
quantity = 10

# Float variables
price = 19.0
pi = 3.14159

# Boolean variables
is_active = True
is_student = False

# List variable
fruits = ["apple", "banana", "orange"]

# Dictionary variable
person = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
}

# Printing variables
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Price: {price}")
print(f"Is Active: {is_active}")
print(f"Fruits: {fruits}")
print(f"Person: {person}")

